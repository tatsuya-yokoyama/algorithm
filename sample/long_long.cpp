#include <iostream>

using namespace std;

int main() {
  int a_i = 10000;
  long long a_ll = 10000;

  cout << "squared" << endl;
  cout << "int: " << a_i * a_i * a_i << endl;
  cout << "long long: " << a_ll * a_ll * a_ll << endl;

  cout << "cubed" << endl;
  cout << "int: " << a_i * a_i * a_i << endl;
  cout << "long long: " << a_ll * a_ll * a_ll << endl;
}
